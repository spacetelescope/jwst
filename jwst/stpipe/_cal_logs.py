def _scrub(msg):
    """
    Scrub sensitive information from a message.

    This includes user and hostnames, IP addresses, and absolute paths.
    Absolute paths are removed by simply checking for a leading backslash,
    and they are replaced with just their root (i.e., the filename).
    Relative paths are not modified.

    Parameters
    ----------
    msg : str
        The string to scrub

    Returns
    -------
    scrubbed : str
        The scrubbed string
    """
    # replace absolute paths with just the filename
    # keep all relative paths as-is
    msg = re.sub(
        r"(?<!\w)(/(?:[^/\s]+/)+([^/\s]+))",
        lambda m: m.group(2),
        msg,
    )
    # Scrub anything containing username or hostname
    # Paths containing usernames should already be removed
    if _USER in msg:
        return ""
    if _HOSTNAME in msg:
        return ""
    if re.search(_IP_REGEX, msg):
        return ""
    return msg


class _ScrubbingFormatter(logging.Formatter):
    """Formatter that removes sensitive information."""

    def format(self, record):
        return _scrub(super().format(record))


_LOG_FORMATTER = _ScrubbingFormatter(
    "%(asctime)s.%(msecs)03d - %(name)s - %(levelname)s - %(message)s",
    datefmt="%Y-%m-%dT%H:%M:%S",
)
_LOG_FORMATTER.converter = time.gmtime
